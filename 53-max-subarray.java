class Solution {
    public int maxSubArray(int[] nums) {

        if (nums == null || nums.length == 0) {
            return 0;
        }
        int max = Integer.MIN_VALUE;
        int pre = 0;
        for (int i=0; i<nums.length; i++) {
            if (pre > 0) {
                pre = pre + nums[i];
            }else {
                pre = nums[i];
            }
            max = Math.max(pre, max);
        }
        return max;
    }
}
