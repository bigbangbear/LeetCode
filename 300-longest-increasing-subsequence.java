class Solution {
    public int lengthOfLIS(int[] nums) {
        if (nums == null || nums.length == 0) {
            return 0;
        }
        int max = 0;
        int[] dp = new int[nums.length];
        for (int i=0; i< nums.length; i++) {
            int length = 1;
            for (int j=0; j<i; j++) {
                if (nums[j] < nums[i]) {
                    length = Math.max(length, dp[j] + 1);
                }
            }
            dp[i] = length;
            max = Math.max(max, length);
        }
        return max;
    }
}
